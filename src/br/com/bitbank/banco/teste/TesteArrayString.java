package br.com.bitbank.banco.teste;

public class TesteArrayString {

	public static void main(String[] args) {
		// Laço de repetição para exibir os dados dentro de args
		for (int i = 0; i < args.length; i++) {
			System.out.println(args[i]);
		}
	}
	/* Esse teste de array serve para que possamos passar parâmetros dentro da String[] args 
	 * Para passar esses parâmetros:
	 * - clicar na seta preta do Run TesteArrayString
	 * - Run Configurations
	 * - Aba Arguments -> Programm Arguments -> Digite qualquer coisa...
	 * - clicar em Run TesteArrayString
	 * - Eclipse irá exibir as informações que você colocou dentro do String[] args
	 * - Obs.: é possível realizar esse procedimento pelo terminal cmd.
	 */

}
